#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*ptr;

	if (!lst || !del)
		return ;
	ptr = lst;
	lst = lst -> next;
	(*del)(ptr -> content);
	free (ptr);
}
