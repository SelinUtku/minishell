#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	char	*dst;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	dst = (char *)malloc((len + 1) * sizeof(char));
	if (!dst)
		return (0);
	while (i < len)
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
